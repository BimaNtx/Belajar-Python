# Soal Praktikum 7: Operator Aritmatika

# Deklarasi variabel
x = 15
y = 4

# Nilai awal
print("Nilai awal:")
print("x =", x)
print("y =", y)

# 1. Penjumlahan
hasil_tambah = x + y 
print("1. Penjumlahan (x + y)       =", hasil_tambah)

# 2. Pengurangan
hasil_kurang = x - y 
print("2. Pengurangan (x + y)       =", hasil_kurang)

# 3. Perkalian
hasil_kali = x * y 
print("3. Perkalian (x + y)         =", hasil_kali)

# 4. Pembagian
hasil_bagi = x / y 
print("4. Pembagian (x / y)         =", hasil_bagi)

# 5. modulus
hasil_modulus = x % y 
print("3. Modulus (x % y)           =", hasil_modulus)

# 6. pangkat
hasil_pangkat = x ** y 
print("6. Pangkat (x ** y)          =", hasil_pangkat)

# 7. floor div atau pembagian bulat
hasil_floor_div = x // y 
print("7. Pembagian Bulat (x // y)  =", hasil_floor_div)