# Soal Praktikum 9: Operator Logika

# Nilai awal variabel logika
x = True
y = False

# Menampilkan nilai awal
print("Nilai x =", x)
print("Nilai y =", y)

# Operator logika AND (dan)
print("1. x and y   -->", x and y)  
# True and False = False

# Operator logika OR (atau)
print("2. x or y    -->", x or y)
# True or False = True

# Operator logika NOT (kebalikan)
print("3. not x     -->", not x)
# not True = False

print("4. not y     -->", not y)
# not False = True
