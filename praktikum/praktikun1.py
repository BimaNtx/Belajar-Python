# 1. Nama lengkap sebagai string
nama_lengkap = "Bima Ananta"

# 2. Usia sebagai integer
usia = 17

# 3. Tinggi badan sebagai float
tinggi_badan = 175.5

# 4. Status siswa aktif sebagai boolean
status_siswa_aktif = True

# Menampilkan nilai dan tipe data dari masing-masing variabel
print(f"Variabel 'nama_lengkap':")
print(f"  Nilai: {nama_lengkap}")
print(f"  Tipe data: {type(nama_lengkap)}\n")

print(f"Variabel 'usia':")
print(f"  Nilai: {usia}")
print(f"  Tipe data: {type(usia)}\n")

print(f"Variabel 'tinggi_badan':")
print(f"  Nilai: {tinggi_badan}")
print(f"  Tipe data: {type(tinggi_badan)}\n")

print(f"Variabel 'status_mahasiswa_aktif':")
print(f"  Nilai: {status_siswa_aktif}")
print(f"  Tipe data: {type(status_siswa_aktif)}")