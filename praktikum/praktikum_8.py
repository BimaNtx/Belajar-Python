# Soal Praktikum 8: Operator Perbandingan

# Nilai awal variabel
a = 10
b = 7

# Menampilkan nilai variabel
print("Nilai a =", a)
print("Nilai b =", b)
print("-" * 35)  # Garis pembatas

# Operator perbandingan satu per satu

# 1. Lebih besar dari
print("1. Apakah a > b?     -->", a > b)

# 2. Lebih kecil dari
print("2. Apakah a < b?     -->", a < b)

# 3. Sama dengan
print("3. Apakah a == b?    -->", a == b)

# 4. Tidak sama dengan
print("4. Apakah a != b?    -->", a != b)

# 5. Lebih besar atau sama dengan
print("5. Apakah a >= b?    -->", a >= b)

# 6. Lebih kecil atau sama dengan
print("6. Apakah a <= b?    -->", a <= b)
